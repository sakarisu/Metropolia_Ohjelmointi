class Auto:
    nopeus = 0
    kilsat = 0
    def __init__(self, rekkari, huiput):
        self.rekkari = rekkari
        self.huiput = huiput
auto = Auto('ABC-123', 142)
print(f"Auton rekisteritunnus on {auto.rekkari:s} ja huippunopeus on {auto.huiput:d} km/h.")
print(f"Auton tämänhetkinen nopeus on {auto.nopeus:d} ja kuljettuja kilometrejä on kertynyt {auto.kilsat:d}.")
